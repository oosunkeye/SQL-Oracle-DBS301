1. SELECT (MAX(SALARY) - MIN(SALARY)) AS "BIG DIFFERENCE" FROM EMPLOYEES

2. SELECT JOB_ID, MAX(SALARY) AS "HIGH", MIN(SALARY) AS "LOW", AVG(SALARY) AS "AVG"
   FROM EMPLOYEES
   GROUP BY JOB_ID
   ORDER BY MIN(SALARY), AVG(SALARY);

3. SELECT DEPARTMENT_ID AS "DEPT#", JOB_ID "JOB TITLE", COUNT(JOB_ID) AS "HOW MANY" FROM EMPLOYEES
   GROUP BY DEPARTMENT_ID, JOB_ID
   HAVING COUNT(JOB_ID) > 2
   ORDER BY COUNT(JOB_ID) DESC;

4. SELECT JOB_ID AS "JOB TITLE", SUM(SALARY) AS "TOTAL PAYROLL" FROM EMPLOYEES
   WHERE JOB_ID NOT LIKE 'AD_VP' AND JOB_ID NOT LIKE 'SA_REP'
   GROUP BY JOB_ID
   HAVING MAX(SALARY) > 12000
   ORDER BY SUM(SALARY) DESC;

5. SELECT MANAGER_ID AS "MGR#", COUNT(EMPLOYEE_ID) AS "HOW MANY" FROM EMPLOYEES
   WHERE MANAGER_ID NOT IN(100, 300)
   GROUP BY MANAGER_ID
   HAVING COUNT(EMPLOYEE_ID) > 2
   ORDER BY MANAGER_ID DESC;

6. SELECT DEPARTMENT_ID, MAX(HIRE_DATE) AS " LATEST", MIN(HIRE_DATE) AS "EARLIEST" FROM EMPLOYEES
   WHERE DEPARTMENT_ID < 60
   GROUP BY DEPARTMENT_ID
   HAVING MIN(HIRE_DATE) > '90-01-03' AND MAX(HIRE_DATE) < '00-01-29'
   ORDER BY MAX(HIRE_DATE) ASC;