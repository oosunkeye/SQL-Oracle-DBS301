1. SELECT EMPLOYEE_ID,LAST_NAME, SALARY FROM EMPLOYEES
   WHERE SALARY BETWEEN 5000 AND 9000
   ORDER BY SALARY DESC, LAST_NAME DESC;
      
2. SELECT EMPLOYEE_ID,LAST_NAME, SALARY FROM EMPLOYEES
   WHERE SALARY BETWEEN 5000 AND 9000 AND (LOWER(JOB_ID) = 'IT_PROG' OR LOWER(JOB_ID) = 'ST_CLERK')
   ORDER BY SALARY DESC, LAST_NAME DESC;

3. SELECT EMPLOYEE_ID,LAST_NAME, SALARY FROM EMPLOYEES
   WHERE SALARY NOT BETWEEN 5000 AND 9000 AND (LOWER(JOB_ID) = 'IT_PROG' OR LOWER(JOB_ID) = 'ST_CLERK')
   ORDER BY SALARY DESC, LAST_NAME DESC;

4. SELECT LAST_NAME, HIRE_DATE, SALARY FROM EMPLOYEES
   WHERE HIRE_DATE < '94-06-07'
   ORDER BY HIRE_DATE DESC;

5. SELECT LAST_NAME, HIRE_DATE, SALARY, DEPARTMENT_ID FROM EMPLOYEES
   WHERE SALARY < 7500
   ORDER BY DEPARTMENT_ID DESC, SALARY DESC;

